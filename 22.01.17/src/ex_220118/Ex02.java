package ex_220118;

public class Ex02 {

	public static void main(String[] args) {
		
	// 숫자 + 숫자 = 숫자
		
	// 숫자 + 문자 = 
		
	int num1 = 10;
	int num2 = 7;
 
			
	// 출력명령 자동완성 : syso + ctrl + space bar
	System.out.println(num1 + num2);
	
	// 숫자 +(연결고리) 문자 = 문자
	int num3 = 2;
	String num4 = "1";
	
	System.out.println(num3 + num4);
	
	
	
	
		
		
		

	}

}
