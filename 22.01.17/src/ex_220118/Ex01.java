package ex_220118;


public class Ex01 {

	public static void main(String[] args) {

// 1. 정수형의 변수 num1을 생성하고 10 데이터 저장하기
	int num1 = 10;	
		
// 2. 정수형의 변수 num2를 생성하고 7 데이터 저장하기
	int num2 = 7;
	
// 3. num1을 num2로 나눈 몫의 값을 출력하기
// 출력하는 명령문 → System.out.println();
	System.out.println(num1/num2);	
// 정수형와 정수형을 연산하면 무조건 결과 값은 정수형이 된다.
// 정수형과 실수형을 연산하면 연산의 결과는 실수형으로 나타난다.
	
// 4. num1과 num2의 나머지 값을 출력하기
	System.out.println(num1%num2);	
	
	}

}
